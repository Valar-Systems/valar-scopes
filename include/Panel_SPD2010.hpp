// SPD2010 412x412 round IPS panel, driven over the 4-data-line QSPI bus.
//
// The SPD2010 (a TDDI controller -- it is also the touch chip, see Touch_SPD2010) speaks the same
// QSPI display protocol as the AMOLED parts LovyanGFX already supports (CO5300 / SH8601Z): commands
// are framed as 0x02 0x00 <cmd> 0x00 and pixel data as 0x32, which is exactly what lgfx::Panel_AMOLED
// emits. So we get every drawing primitive, brightness, sprites, etc. for free by subclassing it and
// only supplying (a) the 412x412 geometry and (b) the SPD2010 vendor init register stream.
//
// The init blob below is mechanically translated from Arduino_GFX's spd2010_init_operations[] into
// LovyanGFX's command_list format: each record is { cmd, num_params, params..., [delay_ms] }, where
